import java.io.*;
import java.util.*;
import java.text.*;
import java.math.*;
import java.util.regex.*;

public class Solution {

    public static void main(String[] args) {
        Scanner s=new Scanner(System.in);
        int i=1;
        while(s.hasNext()){
            String st=s.nextLine();
            System.out.printf("%d "+st+"\n",i);
            i++; 
        }
    }
}

